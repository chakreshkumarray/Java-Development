public class AND {
    public static void main(String[] args) {

        int marks=35;
        int age=25;
        System.out.println(marks >= 33 && age <= 18);

        boolean a=true;
        boolean b=false;
        System.out.println(a && b);

        boolean exp1=2<1;
        boolean exp2=1<=3;
        System.out.println(exp1 && exp2);
    }
}
