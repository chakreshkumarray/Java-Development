public class LessThan {
    public static void main(String[] args) {

        int a = 25;
        int b = 30;
        System.out.println(a < b);

        char c = 'p';
        char d = 'P';
        System.out.println('p'+0);
        System.out.println('P'+0);
        System.out.println(c < d);
    }
}
