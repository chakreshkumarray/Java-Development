package WhileLoop;

public class WhileLoop {
    public static void main(String[] args) {

        int i = 1;                    // Initialization
        while (i <= 10){              // Condition
            System.out.println(i);
            i++;                      // Update
        }

        int count = 500;
        while (count >= 200){
            System.out.println(count);
            count--;
        }

    }
}
