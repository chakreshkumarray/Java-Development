public class Escape {
    public static void main(String[] args) {

        System.out.println("Hello \"Chakresh\"");
        System.out.println("Hello \'Chakresh\'");
        System.out.println("Hello \t Chakresh");
        System.out.println("Hello \n Chakresh");
        System.out.println("Hello \b Chakresh");
        System.out.println("Hello \\ Chakrresh\\");
    }
}
