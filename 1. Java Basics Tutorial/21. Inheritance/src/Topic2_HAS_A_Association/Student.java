package Topic2_HAS_A_Association;

public class Student {

    String name;
    int rollNumber;
    /*
    This is Association because --> Student attached with name
                                --> Student attached with rollNumber
                                --> This relationship called Association for class with object
                                --> establish relation between class and object
                                --> student has-a name
                                --> student has-a rollNumber

       Association --> Aggregation -> weak bonding --> does not depend on method(class)
                   --> Composition -> strong bonding --> depend on method(class)
     */
}
