public class A10_UnaryOperator {
    public static void main(String[] args) {

        int x = 13;
        int y = 15;

        // Pre -Increment
        System.out.println(++x);

        // Pre-Decrement
        System.out.println(--y);

        // Post Increment
        System.out.println(x++);

        // Post Decrement
        System.out.println(y--);

    }
}
