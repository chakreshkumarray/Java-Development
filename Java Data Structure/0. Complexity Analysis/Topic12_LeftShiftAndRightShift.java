public class Topic12_LeftShiftAndRightShift {
    public static void main(String[] args) {

        int n = 15;
        int m = 14;

        System.out.println(n<<2);
        System.out.println(n>>2);
        System.out.println(n & m);
        System.out.println(n | m);
        System.out.println(n ^ m);
    }
}
