public class Arithmetic_Deep_Dive {
    public static void main(String[] args) {

        int a = 5;
        double b = 10.1;
        double c = a + b;
        System.out.println(c);

        float x = 12.3f;
        long y = 151513;
        float z = x + y;
        System.out.println(z);

        long e = 482121645;
        long d = e * 24235;
        System.out.println(d);

        int p = 10;
        int q = 3;
        int r = p / q;
        System.out.println(r);

        double p1 = 10;
        int q1 = 3;
        double r1 = p1 / q1;
        System.out.println(r1);

        float m = 10.3f;
        int n = 3;
        float o = m / n;
        System.out.println(o);
    }
}

