public class LessThanEqual {
    public static void main(String[] args) {

        int a = 25;
        int b = 23;
        System.out.println(a <= b);

        char c = 'h';
        char d = 'H';
        System.out.println('h'+0);
        System.out.println('H'+0);
        System.out.println(c <= d);
    }
}
