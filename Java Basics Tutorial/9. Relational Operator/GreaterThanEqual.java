public class GreaterThanEqual {
    public static void main(String[] args) {

        int a = 15;
        int b = 24;
        System.out.println(a>=b);

        char str1 = 'f'; // 120
        char str2 = 'N'; // 78
        System.out.println('f'+0);
        System.out.println('N'+0);
        System.out.println(str1 >= str2);

    }
}
